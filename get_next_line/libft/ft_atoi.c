#include "libft.h"

int		ft_atoi(const char *s)
{
	size_t		i;
	size_t		n;
	int			c;

	i = 0;
	n = 0;
	c = 1;
	while (s[i] == '\n' || s[i] == '\t' || s[i] == '\v' || s[i] == '\r' ||
			s[i] == '\f' || s[i] == 32)
		i++;
	if (s[i] == '-' && (s[i + 1] >= '0' && s[i + 1] <= '9'))
	{
		c = -1;
		i++;
	}
	else if (s[i] == '+' && s[i + 1] >= '0' && s[i + 1] <= '9')
		i++;
	while (s[i] >= '0' && s[i] <= '9')
		n = n * 10 + s[i++] - '0';
	n *= c;
	return (n);
}
